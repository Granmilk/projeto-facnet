
public class Produto {
	
	  String nome;
	  long códigoDeIdentificação;
	  double preço;
      int quantidade;
      
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public long getCódigoDeIdentificação() {
		return códigoDeIdentificação;
	}
	public void setCódigoDeIdentificação(long códigoDeIdentificação) {
		this.códigoDeIdentificação = códigoDeIdentificação;
	}
	public double getPreço() {
		return preço;
	}
	public void setPreço(double preço) {
		this.preço = preço;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	 
	 

}
